function FaqItem({ item, open, onToggle }) {
  return (
    <div className="border border-slate-100 rounded-xl overflow-hidden">
      <button
        type="button"
        onClick={onToggle}
        className="w-full flex items-center justify-between gap-3 px-4 py-3 text-left hover:bg-slate-50 transition-colors"
      >
        <span className="text-sm font-semibold text-slate-700">{item.q}</span>
        <ChevronDown
          size={16}
          className="flex-shrink-0 text-slate-400 transition-transform duration-200"
          style={{ transform: open ? 'rotate(180deg)' : 'rotate(0deg)' }}
        />
      </button>
      {open && (
        <div className="px-4 pb-3 pt-0 text-sm text-slate-500 leading-relaxed">{item.a}</div>
      )}
    </div>
  );
}

export default function HelpModal({ onClose }) {
  const [openIndex, setOpenIndex] = useState(0);

  return (
    <Modal title="ช่วยเหลือ / คำถามที่พบบ่อย" onClose={onClose} size="lg">
      <div className="space-y-2">
        {FAQS.map((item, i) => (
          <FaqItem
            key={i}
            item={item}
            open={openIndex === i}
            onToggle={() => setOpenIndex(openIndex === i ? -1 : i)}
          />
        ))}
      </div>
    </Modal>
  );
}
